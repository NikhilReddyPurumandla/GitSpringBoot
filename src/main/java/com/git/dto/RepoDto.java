package com.git.dto;

public class RepoDto {
String id;
String repo;
String umail;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getRepo() {
	return repo;
}
public void setRepo(String repo) {
	this.repo = repo;
}
public String getUmail() {
	return umail;
}
public void setUmail(String umail) {
	this.umail = umail;
}

}
